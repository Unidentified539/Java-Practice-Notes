package methods;

public class Iteratate3 {
    public void itInt(int[] itThis) {
        int i, x;

        for (i = 0; i < itThis.length; i++) {
            x = itThis[i];
            System.out.println(x);
        }
    }
    public void itString(String[] itThis) {
        int i;
        String x;

        for (i = 0; i < itThis.length; i++) {
            x = itThis[i];
            System.out.println(x);
        }
    }
}
